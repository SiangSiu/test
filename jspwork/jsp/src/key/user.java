package key;

public class user {

	String name;
	int num;
	
	public user(){
		name=null;
		num=0;
	}
	
	public String getname() {
		return name;
	}

	public void setname(String name) {
		this.name = name;
	}

	public int getnum() {
		return num;
	}

	public void setnum(int num) {
		this.num = num;
	}
}
